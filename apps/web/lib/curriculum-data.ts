// ─── Lookup helpers ────────────────────────────────────────────────────────

export function getCourse(courseSlug: string) {
  return CURRICULUM.find((c) => c.slug === courseSlug)
}

export function getCourseLevel(courseSlug: string, levelSlug: string) {
  return getCourse(courseSlug)?.levels.find((l) => l.slug === levelSlug)
}

export function getCourseModule(courseSlug: string, levelSlug: string, moduleSlug: string) {
  return getCourseLevel(courseSlug, levelSlug)?.modules.find((m) => m.slug === moduleSlug)
}

export function getNextModule(courseSlug: string, levelSlug: string, moduleSlug: string) {
  const level = getCourseLevel(courseSlug, levelSlug)
  if (!level) return null
  const idx = level.modules.findIndex((m) => m.slug === moduleSlug)
  if (idx === -1 || idx === level.modules.length - 1) return null
  return level.modules[idx + 1]
}
